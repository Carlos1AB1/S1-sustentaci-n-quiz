package Operadores_Incremento_Drecremento;

public class Operadores_Incremento_Decremento {

    public static void main(String[] args) {

        int contador = 5;
        System.out.println("Valor inicial del contador: " + contador);

        contador++;
        System.out.println("Después del incremento: " + contador);

        int numero = 10;
        System.out.println("Valor inicial del número: " + numero);

        numero--;
        System.out.println("Después del decremento: " + numero);
    }
}
